# aula de operadores aritméticos
# ** exponenciação, // divisão inteira, % resto da divisão
# ordem de precedência 1º (), 2º **, 3º * / // %, 4º + -
n = input('Qual é o seu nome?')
print('Prazer em te conhecer {:^20}!'.format(n))
print('!{:<20}'.format(n))
print('{:>42}!'.format(n))
print('{:~^42}'.format(n))

